import type { Point } from "../types";

export interface PalletSpawnPoint {
  id: string;
  name: string;
  slots: {
    id: string;
    x: number;
    y: number;
    cant_pallets?: number;
  }[];
  zone: 'mixto' | 'carga' | 'descarga' | 'temporal' | 'completo' | 'espera' | 'descarga distribucion' | 'descarga t1-t2';
}
export interface RuntimePallet {
  id: string;
  zoneId: string;             // "temporary-zone"
  slotId: string;             // temp-slot-1, temp-slot-2, ...
  createdAtSimSec: number;    // tiempo de simulación
  label: string;
  camionAsignado?: string | null;

  inTransit?: boolean;
  fromZoneId?: string;
  fromSlotId?: string;
  toZoneId?: string;
  toSlotId?: string;
  transitStartSimSec?: number;
  transitEndSimSec?: number;
  xNorm?: number;
  yNorm?: number;

  pathNorm?: Point[]; // camino pre-calculado
  dropOnTruck?: boolean;
  dropTruckId?: string | null;

  isBeingChecked?: boolean;          // true mientras dura el evento de chequeo
  isCheckedOk?: boolean;             // true cuando el chequeo ya terminó
  lastCheckStartSimSec?: number;     // inicio del último chequeo (si existe)
  lastCheckEndSimSec?: number;  
  
}



